def prepare_ltf(df: pd.DataFrame) -> pd.DataFrame:
    work = add_indicators(df.copy())
    work["range"] = (work["high"] - work["low"]).replace(0, np.nan)
    work["body_strength"] = ((work["close"] - work["open"]).abs() / work["range"]).fillna(0.0)
    upper = work["high"] - np.maximum(work["open"], work["close"])
    lower = np.minimum(work["open"], work["close"]) - work["low"]
    work["wick_ratio"] = (np.maximum(upper, lower) / work["range"]).fillna(0.0)
    vol_ma = work["volume"].rolling(20, min_periods=1).mean().replace(0, np.nan)
    work["volume_impulse"] = (work["volume"] / vol_ma).fillna(1.0)
    typical = (work["high"] + work["low"] + work["close"]) / 3.0
    work["rolling_vwap"] = (typical * work["volume"]).rolling(96, min_periods=1).sum() / work["volume"].rolling(96, min_periods=1).sum().replace(0, np.nan)
    work["vwap_distance"] = ((work["close"] - work["rolling_vwap"]) / work["rolling_vwap"].replace(0, np.nan)).fillna(0.0)
    return work


def round_price(price: float) -> float:
    return round(round(price / PRICE_TICK) * PRICE_TICK, 8)


def round_size(size: float) -> float:
    return np.floor(size / SIZE_STEP) * SIZE_STEP


def safe_div(num: float, den: float, default: float = 0.0) -> float:
    return float(num / den) if den and not pd.isna(den) else default


def session_name(open_time: int) -> str:
    hour = pd.to_datetime(open_time, unit="ms", utc=True).hour
    if 0 <= hour < 8:
        return "tokyo"
    if 8 <= hour < 13:
        return "london"
    if 13 <= hour < 21:
        return "ny"
    return "off"


