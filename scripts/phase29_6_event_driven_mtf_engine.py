def add_teacher_comparison(row: dict[str, Any], trades: pd.DataFrame, teacher: pd.DataFrame) -> dict[str, Any]:
    out = dict(row)
    live_ts = time_side_key_set(trades)
    teach_ts = time_side_key_set(teacher)
    exact = basic_key_set(trades)
    teach_exact = basic_key_set(teacher)
    out["teacher_time_side_match_count"] = len(live_ts & teach_ts)
    out["teacher_time_side_match_rate"] = safe_div(len(live_ts & teach_ts), len(teach_ts))
    out["missing_teacher_trades"] = len(teach_ts - live_ts)
    out["extra_live_trades"] = len(live_ts - teach_ts)
    out["exact_trade_matches"] = len(exact & teach_exact)
    return out


def traceability_rows(trades: pd.DataFrame) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    if trades.empty:
        return rows
    views = [
        ("first_20", trades.head(20)),
        ("last_20", trades.tail(20)),
        ("largest_winners", trades.sort_values("net_pnl", ascending=False).head(10)),
        ("largest_losers", trades.sort_values("net_pnl", ascending=True).head(10)),
    ]
    monthly = monthly_table(trades)
    if not monthly.empty:
        best_month = str(monthly.sort_values("pnl", ascending=False).iloc[0]["month"])
        worst_month = str(monthly.sort_values("pnl", ascending=True).iloc[0]["month"])
        t = trades.copy()
        t["month"] = pd.to_datetime(t["entry_time"], unit="ms", utc=True).dt.to_period("M").astype(str)
        views.append(("best_month_trace", t[t["month"] == best_month].head(30)))
        views.append(("worst_month_trace", t[t["month"] == worst_month].head(30)))
    for label, frame in views:
        for _, row in frame.iterrows():
            rows.append({
                "trace_type": label,
                "entry_time": row.get("entry_time", ""),
                "entry_datetime": row.get("entry_datetime", ""),
                "exit_time": row.get("exit_time", ""),
                "side": row.get("side", ""),
                "sleeve": row.get("sleeve", ""),
                "entry_model": row.get("entry_model", ""),
                "exit_reason": row.get("exit_reason", row.get("reason", "")),
                "net_pnl": row.get("net_pnl", ""),
                "R": row.get("R", ""),
            })
    return rows


